# Load the sound effects
pygame.mixer.init()
dot_sound = pygame.mixer.Sound("eating-chips-81092.wav")
collision_sound = pygame.mixer.Sound("mixkit-retro-arcade-lose-2027.wav")

def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)
        
        # Play the dot eating sound effect
        dot_sound.play()

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

        # Check collision with Pacman
        if abs(pacman - point) < 20:
            # Play the collision sound effect
            collision_sound.play()
            state['game_over'] = True
            return

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def game_over():
    """Display game over message."""
    writer.goto(-50, 0)
    writer.color('red')
    writer.write("Game Over", font=("Arial", 20, "bold"), align="center")


def change(x, y):
    """Change pacman aim if valid."""
    if not state['game_over'] and valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


def play_background_music():
    try:
        pygame.mixer.music.load("Pixel-Peeker-Polka-faster(chosic.com).mp3")
        pygame.mixer.music.play(-1)
    except pygame.error as e:
        print("Error loading background music:", e)


def main():
    setup(420, 420, 370, 0)
    hideturtle()
    tracer(False)
    writer.goto(160, 160)
    writer.color('white')
    writer.write(state['score'])
    listen()
    onkey(lambda: change(5, 0), 'Right')
    onkey(lambda: change(-5, 0), 'Left')
    onkey(lambda: change(0, 5), 'Up')
    onkey(lambda: change(0, -5), 'Down')
    world()
    move()
    done()
    if state['game_over']:
        game_over()
    play_background_music()


